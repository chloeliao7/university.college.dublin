/**
   Prints a staircase.
*/
public class StaircasePrinter
{  
   public static void main(String[] args)
   {  
      System.out.println("            +---+");
      System.out.println("            |   |");
      System.out.println("        +---+---+");
      System.out.println("        |   |   |");
      System.out.println("    +---+---+---+");
      System.out.println("    |   |   |   |");
      System.out.println("+---+---+---+---+");
      System.out.println("|   |   |   |   |");
      System.out.println("+---+---+---+---+");
   }
}




